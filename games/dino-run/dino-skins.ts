const RUN_FRAME_MS = 52;

export function stripePhaseForAlternateRows(): number {
  return 0;
}

/** Drop leading/trailing all-empty rows so feet align with bottom of draw box (fixes “floating” duck when the grid has padded blank edges). */
function trimCharsetRowsForDraw(rows: readonly string[]): readonly string[] {
  const hasInk = (s: string) => /[#O]/.test(s);
  let a = 0;
  let b = rows.length - 1;
  while (a < rows.length && !hasInk(rows[a] ?? "")) a++;
  while (b > a && !hasInk(rows[b] ?? "")) b--;
  return rows.slice(a, b + 1);
}

/** Paints charset cells; when `alternateRowShading` is false (duck), `#` uses only `body` so striped rows cannot read as stray leg marks. */
function fillCharsetCells(
  g: CanvasRenderingContext2D,
  topLeftX: number,
  topLeftY: number,
  cell: number,
  rows: readonly string[],
  stripePhase: number,
  body: string,
  shade: string,
  eye: string,
  alternateRowShading: boolean,
): void {
  for (let r = 0; r < rows.length; r++) {
    const row = rows[r] ?? "";
    for (let c = 0; c < row.length; c++) {
      const ch = row[c];
      if (ch === "#") {
        if (alternateRowShading && ((r + stripePhase) & 1)) g.fillStyle = shade;
        else g.fillStyle = body;
        g.fillRect(topLeftX + c * cell, topLeftY + r * cell, cell, cell);
      } else if (ch === "O") {
        g.fillStyle = eye;
        g.fillRect(topLeftX + c * cell, topLeftY + r * cell, cell, cell);
      }
    }
  }
}

export type DinoPalette = {
  readonly body: string;
  readonly shade: string;
  /** Eye highlight (offline game uses white); kept fixed when colouring the body. */
  readonly eye?: string;
};

/** Draw dinosaur; body/shade stripe `#`, eye uses `palette.eye ?? #ffffff`. */
export function drawSkinDino(
  g: CanvasRenderingContext2D,
  pb: { x: number; y: number; w: number; h: number },
  palette: DinoPalette,
  phase: "idle" | "running" | "dead",
  grounded: boolean,
  isDuck: boolean,
  runTime: number,
): void {
  const sprites = OFFLINE_DINO_SPRITES;
  const { body, shade } = palette;
  const eye = palette.eye ?? "#ffffff";
  const stripePhase = stripePhaseForAlternateRows();

  if (isDuck) {
    /** Alternate duck frames each run-cycle tick (~52ms), same tempo as limb swap in Chrome offline. */
    const spriteRows =
      Math.floor((runTime * 1000) / RUN_FRAME_MS) % 2 === 0
        ? sprites.duck0
        : sprites.duck1;
    const rows = trimCharsetRowsForDraw(spriteRows);
    let maxW = 0;
    for (const row of rows) maxW = Math.max(maxW, row.length);
    const maxH = rows.length;
    const cell = Math.min(pb.w / maxW, pb.h / maxH);
    const drawW = maxW * cell;
    const drawH = maxH * cell;
    const ox = pb.x + (pb.w - drawW) * 0.5;
    const oy = pb.y + (pb.h - drawH);
    fillCharsetCells(g, ox, oy, cell, rows, stripePhase, body, shade, eye, false);
    return;
  }

  let rows: readonly string[];
  if (phase !== "running") {
    rows = sprites.runFrames[0];
  } else if (!grounded) {
    rows = sprites.jump;
  } else {
    const runIdx =
      Math.floor((runTime * 1000) / RUN_FRAME_MS) % sprites.runFrames.length;
    rows = sprites.runFrames[runIdx];
  }

  let maxW = 0;
  for (const row of rows) maxW = Math.max(maxW, row.length);
  const maxH = rows.length;
  const cell = Math.min(pb.w / maxW, pb.h / maxH);
  const drawW = maxW * cell;
  const drawH = maxH * cell;
  const ox = pb.x + (pb.w - drawW) * 0.5;
  const oy = pb.y + (pb.h - drawH);

  fillCharsetCells(g, ox, oy, cell, rows, stripePhase, body, shade, eye, true);
}
